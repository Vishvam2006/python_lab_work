def marks_avg(math,phy,chem,com,bio):
    total = math+phy+chem+com+bio
    avg = total/5
    print('The total is :',total ,'The avg is :',avg)
math = int(input('Enter the marks of maths'))
phy = int(input('Enter the marks of maths'))
chem = int(input('Enter the marks of maths'))
com = int(input('Enter the marks of maths'))
bio = int(input('Enter the marks of maths'))
marks_avg(math,phy,chem,com,bio)
